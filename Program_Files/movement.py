def InputHandling(Room):
    def ReplaceInput():
            print("\033[2A", end="")
            print('Error With Input')
            NextMove = ''
            InputHandling(Room)

    NextMove = input(f'What do You want to do? ')

    if NextMove == '0':
        UI.DisplayInventoryScreen()

    elif Room == 'Forest1':
        if NextMove == '1':
            UI.PrintMainUI('Enemy1')
        else:
            ReplaceInput()

    elif Room == 'Enemy1':
        if NextMove == '1':
            UI.PrintMainUI('Enemy1')
            
        elif NextMove == '0':
            pass
        else:
            ReplaceInput()

